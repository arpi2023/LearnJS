//Logical Operators
//AND, OR , NOT

console.log ((3>2) && (4>3))
console.log ((3>2) && (4<3))
console.log ((3<2) && (4>3))
console.log ((3<2) && (4<3))



//OR operator

console.log((3>2) || (4>3))
console.log((3>2) || (4<3))
console.log((3<2) || (4>3))
console.log((3<2) || (4<3))

//NOT operator

console.log(!(3>2))
console.log (!("3" === 3))
console.log (!("3" == 3))